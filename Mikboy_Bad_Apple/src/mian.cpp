void draw_rle_image(const uint8_t* data, uint32_t size)
{
    static uint16_t line_buffer[2][159];
    uint8_t buf_id = 0;
    uint32_t i = 0;
    uint32_t pixel_count = 0;
    uint16_t x = 0;
    while (i < size && pixel_count < (159 * 129))
    {
        uint8_t run = data[i++];
        uint8_t value = data[i++];

        for (uint8_t r = 0; r < run; r++)
        {
            for (int bit = 7; bit >= 0; bit--)
            {
                if (pixel_count >= (159 * 129)) break;
                uint8_t pixel = (value >> bit) & 1;
                line_buffer[buf_id][x++] = pixel ? 0xFFFF : 0x0000;
                pixel_count++;

                if (x == 159)
                {
                    line_counter++;
                    if (line_counter >= 50) 
                    {
                        line_counter = 0;
                        if (need_fill)
                        {
                            need_fill = 0;
                            if (active_buf == 0) fill_dac_buf(dac_buf1);
                            else fill_dac_buf(dac_buf0);
                        }
                    }
                    mik.lcd().print_mas(line_buffer[buf_id], 159);
                    buf_id ^= 1;
                    x = 0;
                    y++;
                    if (y >= 129)
                    {
                        y = 0;
                        return;
                    }
                }

            }
        }
    }
}

